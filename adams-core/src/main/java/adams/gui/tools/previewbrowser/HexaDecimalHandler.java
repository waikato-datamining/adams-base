package adams.gui.tools.previewbrowser;

import java.io.File;

import adams.core.io.FileUtils;
import adams.gui.core.TextEditorPanel;

/**
 <!-- globalinfo-start -->
 * Displays any file in hexadecimal notation
 * <p/>
 <!-- globalinfo-end -->
 *
 <!-- options-start -->
 * <pre>-logging-level &lt;OFF|SEVERE|WARNING|INFO|CONFIG|FINE|FINER|FINEST&gt; (property: loggingLevel)
 * &nbsp;&nbsp;&nbsp;The logging level for outputting errors and debugging output.
 * &nbsp;&nbsp;&nbsp;default: WARNING
 * </pre>
 * 
 <!-- options-end -->
 *
 * @author  fracpete (fracpete at waikato dot ac dot nz)
 * @version $Revision$
 */
public class HexaDecimalHandler
  extends AbstractContentHandler {

  /** for serialization. */
  private static final long serialVersionUID = 6961566580558582551L;

  /**
   * Returns a string describing the object.
   *
   * @return 			a description suitable for displaying in the gui
   */
  @Override
  public String globalInfo() {
    return "Displays any file in hexadecimal notation";
  }

  /**
   * Returns the list of extensions (without dot) that this handler can
   * take care of.
   *
   * @return		the list of extensions (no dot)
   */
  @Override
  public String[] getExtensions() {
    return new String[]{MATCH_ALL};
  }

  /**
   * Creates the actual view.
   *
   * @param file	the file to create the view for
   * @return		the view
   */
  @Override
  protected PreviewPanel createPreview(File file) {
    TextEditorPanel	panel;

    panel = new TextEditorPanel();
    panel.setEditable(false);
    panel.setContent(FileUtils.loadHexFromBinaryFile(file));

    return new PreviewPanel(panel, panel.getTextArea());
  }
}
