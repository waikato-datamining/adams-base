public class ImageHistogram
  extends AbstractGraphicalDisplay
  implements DisplayPanelProvider {

  private static final long serialVersionUID = -724041933659541058L;

  /**
   * Returns a string describing the object.
   *
   * @return 			a description suitable for displaying in the gui
   */
  @Override
  public String globalInfo() {
    return "Displays a histogram of a gray image or histograms per channel for color image.";
  }

  /**
   * Returns the class that the consumer accepts.
   *
   * @return		the Class of objects that can be processed
   */
  @Override
  public Class[] accepts() {
    return new Class[]{AbstractImageContainer.class};
  }

  /**
   * Creates the panel to display in the dialog.
   *
   * @return		the panel
   */
  @Override
  protected BasePanel newPanel() {
    return new HistogramPanel();
  }

  /**
   * Displays the token (the panel and dialog have already been created at
   * this stage).
   *
   * @param token	the token to display
   */
  @Override
  protected void display(Token token) {
    HistogramPanel		panel;
    AbstractImageContainer	cont;

    cont  = (AbstractImageContainer) token.getPayload();
    panel = (HistogramPanel) m_Panel;
    panel.setImage(cont.toBufferedImage());
  }

  /**
   * Clears the content of the panel.
   */
  @Override
  public void clearPanel() {
    ((HistogramPanel) m_Panel).setImage(null);
  }

  /**
   * Creates a new display panel for the token.
   *
   * @param token	the token to display in a new panel, can be null
   * @return		the generated panel
   */
  @Override
  public DisplayPanel createDisplayPanel(Token token) {
    AbstractComponentDisplayPanel	result;

    result = new AbstractComponentDisplayPanel(getClass().getSimpleName()) {
      HistogramPanel m_Panel;

      @Override
      public void display(Token token) {
	AbstractImageContainer	cont;

	cont    = (AbstractImageContainer) token.getPayload();
	m_Panel = new HistogramPanel();
	m_Panel.setImage(cont.toBufferedImage());
	add(m_Panel, BorderLayout.CENTER);
      }

      @Override
      public void clearPanel() {
	if (m_Panel != null)
	  m_Panel.setImage(null);
      }

      @Override
      public JComponent supplyComponent() {
	if (m_Panel != null)
	  return m_Panel;
	else
	  return null;
      }

      @Override
      public void cleanUp() {
	if (m_Panel != null)
	  m_Panel.setImage(null);
      }
    };

    return result;
  }

  /**
   * Returns whether the created display panel requires a scroll pane or not.
   *
   * @return		true if the display panel requires a scroll pane
   */
  @Override
  public boolean displayPanelRequiresScrollPane() {
    return false;
  }
}
